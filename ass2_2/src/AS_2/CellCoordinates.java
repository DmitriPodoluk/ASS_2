package AS_2;

final public class CellCoordinates {
	final private int col;
	final private int row;
	
	public int getCol() {
		return col;
	}
	public int getRow() {
		return row;
	}
	
	public CellCoordinates(int row, int col) {
	
		this.row = row;
		this.col = col;
	}
	
}
